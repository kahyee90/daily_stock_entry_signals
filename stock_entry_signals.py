    zones = find_support_zones(df)
    price = df['Close'].iloc[-1].item()
    near, support = near_support_zone(price, zones)

    if near and rebound_signal(df, support):
        decision = f"Near support at {support:.2f} – possible entry!"
        signals.append((ticker, support))
    else:
        decision = "No entry signal at the moment."

    # Always show the closest support zone
    if zones:
      closest_zone = min(zones, key=lambda z: abs(price - z['center']))
