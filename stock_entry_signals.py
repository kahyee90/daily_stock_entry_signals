# Checks if price is near support zone
def near_support_zone(price, zones, tolerance=0.03):

  for zone in zones:

    center = zone['center']
    if abs(price-center)/center <= tolerance:
      return True, center

  return False, None

# Detects rebound signals
def rebound_signal(df,support):
  last_close = df['Close'].iloc[-1].item()
  prev_close = df['Close'].iloc[-2].item()
  prev_low = df['Low'].iloc[-2].item()

  if abs(prev_low - support)/support < 0.01 and last_close > prev_close:
    return True

  return False

