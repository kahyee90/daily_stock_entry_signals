# Detects full support zones
def find_support_zones(df):
  swing_lows = get_swing_lows(df)
  #print("Swing lows:", swing_lows)

  if len(swing_lows) == 0:
        return []

  zones = cluster_supports(swing_lows, tolerance=0.03)
